const DEFINE_PROPERTY = 'defineProperty';
function define(O, key, value) {
    O[key] || Object[DEFINE_PROPERTY](O, key, {
        writable: true,
        configurable: true,
        value: value
    });
}

define(String.prototype, 'padLeft', ''.padStart);
define(String.prototype, 'padRight', ''.padEnd);

